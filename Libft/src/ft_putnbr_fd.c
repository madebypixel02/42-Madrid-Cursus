#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		digit;
	char 	c;
	if (n == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		ft_putnbr_fd(147483648, fd);
	}
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		ft_putnbr_fd(-n, fd);
	}
	else if (n > 0 && n <= 2147483647)
	{
		digit = n % 10;
		c = digit + '0';
		ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd(c, fd);
	}
}
